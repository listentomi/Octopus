	"playerID" VARCHAR(255) NOT NULL  ,
	"award" VARCHAR(255) NOT NULL  ,
	"year" INTEGER NOT NULL  ,
	"lgID" VARCHAR(255) NULL  ,
	"note" VARCHAR(255) NULL  ,
	"pos" VARCHAR(255) NULL  ,
	PRIMARY KEY ("playerID", "year", "award"),
