CREATE TABLE "awards_coaches" (
	"id" INTEGER PRIMARY KEY AUTOINCREMENT,
	"year" INTEGER NULL  ,
	"coachID" VARCHAR(255) NULL  ,
	"award" VARCHAR(255) NULL  ,
	"lgID" VARCHAR(255) NULL  ,
	"note" VARCHAR(255) NULL,
	FOREIGN KEY("year") REFERENCES "coaches" ("year") ON UPDATE CASCADE ON DELETE CASCADE,
	FOREIGN KEY("coachID") REFERENCES "coaches" ("coachID") ON UPDATE CASCADE ON DELETE CASCADE
);
