CREATE TABLE "coaches" (
	"coachID" VARCHAR(255) NOT NULL  ,
	"year" INTEGER NOT NULL  ,
	"tmID" VARCHAR(255) NOT NULL  ,
	"lgID" VARCHAR(255) NULL  ,
	"stint" INTEGER NOT NULL  ,
	"won" INTEGER NULL  ,
	"lost" INTEGER NULL  ,
	"post_wins" INTEGER NULL  ,
	"post_losses" INTEGER NULL  ,
	PRIMARY KEY ("coachID", "year", "tmID", "stint"),
	FOREIGN KEY("year") REFERENCES "teams" ("year") ON UPDATE CASCADE ON DELETE CASCADE,
	FOREIGN KEY("tmID") REFERENCES "teams" ("tmID") ON UPDATE CASCADE ON DELETE CASCADE
);
